def matching(automata, texto):
    opts = list(texto)
    est = [0]
    est = utils.cerraduraEpsilon(automata, est)
    q = 0
    while True:
        temporales = []
        for es in est:
            for trans in automata.states[es].transitions:
                if trans.simbolo == opts[q] and trans.destino not in temporales:
                    temporales.append(trans.destino)
        q += 1
        temporales = utils.cerraduraEpsilon(automata, temporales)
        if not temporales and opts == utils.EPSILON:
            break
        est = temporales.copy()
        if q > len(opts)-1:
            break
    for x in est:
        if automata.states[x].accept:
            return True
    return False
def longest_string(file, automata, current = 0):
    temp = ''
    valid = []
    while current < len(file):
        temp += file[current]
        if matching(automata, temp):
            valid.append(temp)
        current += 1
    if valid:
        return max(valid, key = len)
    return False
